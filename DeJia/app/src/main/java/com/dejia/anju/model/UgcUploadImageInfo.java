package com.dejia.anju.model;

public class UgcUploadImageInfo {
    public String show_img_url;
    public String post_img_url;
    public int width;
    public int height;

    public String getShow_img_url() {
        return show_img_url;
    }

    public void setShow_img_url(String show_img_url) {
        this.show_img_url = show_img_url;
    }

    public String getPost_img_url() {
        return post_img_url;
    }

    public void setPost_img_url(String post_img_url) {
        this.post_img_url = post_img_url;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
